const dictionaries = {
  'zh-CN': zhCN,
  en
} as const;

export type TranslationKey = keyof typeof zhCN;

export function translate(
  language: AppLanguage,
  key: TranslationKey,
  variables: Record<string, string | number> = {}
) {
  const template = dictionaries[language][key] || dictionaries.en[key];

  return template.replace(/\{\{(.*?)\}\}/g, (_match, variableName: string) => {
    const value = variables[variableName.trim()];
    return value === undefined ? '' : String(value);
  });
}

export function getDictionarySectionLabel(language: AppLanguage, dictionaryType: DictionaryType) {
  return translate(language, `dictionary.section.${dictionaryType}` as TranslationKey);
}
